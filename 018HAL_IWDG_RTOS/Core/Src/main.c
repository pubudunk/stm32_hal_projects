#define DEBOUNCE_DELAY		50

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{

	static uint32_t last_press_time = 0;

	if(GPIO_Pin == GPIO_PIN_13) {

		uint32_t curr_time = HAL_GetTick();	/* Get the current time in ms */

		/* if the time since last press is greater than the debounce delay. Report the press */
		if(curr_time - last_press_time > DEBOUNCE_DELAY) {
			printf("button pressed\n");
			last_press_time = curr_time;
		}
	}
}

