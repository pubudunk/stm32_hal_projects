void IWDGRefreshTaskHandler(void *argument)
{
	uint32_t wakeUpTime = osKernelGetTickCount();
  for(;;)
  {
	  HAL_IWDG_Refresh(&hiwdg);
	  wakeUpTime += 25;	/* sleep for next 950ms as iwdg timeout in 30ms */
	  osDelayUntil(wakeUpTime);
  }
}

